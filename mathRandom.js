//A JavaScript program to demonstrate Math.random()
console.log(Math.random()); 
// Here Math.random() will generata any random number between 0 & 1

console.log(Math.random() * 50);
//Here Math.random() will generata any random number between 0 & 1 and will multiply that number by 50. So now we have a number between 0 & 50

console.log(Math.floor(Math.random() * 50));
//Here Math.floor rounds the number down to the nearest whole number

