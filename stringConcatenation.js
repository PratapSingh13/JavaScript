//A JavaScript Program to perform string concatenation
console.log('Hello' + 'World');                                   //Output-:HelloWorld
console.log('Hello ' + 'World');                                 //Output-:Hello World
console.log('Hello' + ' World');                                //Output-:Hello World
console.log('Hello' + ' ' + 'World');                          //Output-:Hello World
console.log('One' + ', ' + 'two' + ', ' + 'three!');          //Output-:One, two, three!
