//A JavaScript program to demonstrate String Interpolation
const myLastName = 'Pratap Singh';
console.log(`My name is: ${myLastName}`);                    //Output-:My name is: Pratap Singh

let myFirstName = 'Yogendra';
console.log(`My name is: ${myFirstName} Pratap Singh`);     //Output-:My name is:Yogendra Pratap Singh

const myName = 'Yogendra Pratap Singh';
const myCity = 'Frankfurt';
console.log(`My name is: ${myName}. My favorite city is: ${myCity}`)                 //Output-:My name is Yogendra Pratap Singh. My favorite city is Frankfurt
